<h3>Shortcodes and Parameter Options</h3>

<h4>Toggles Shortcode Example</h4>

<pre>
&#91;toggles title="Toggle Group Title" active=1 speed=500&#93;
	&#91;toggle title="First toggle title"&#93;First toggle text&#91;/toggle&#93;
	&#91;toggle title="Second toggle title"&#93;Second toggle text&#91;/toggle&#93;	
&#91;/toggles&#93;
</pre>

<h4>Tabs Shortcode Example</h4>

<pre>
&#91;tabs title="Tabs Group Title" active=2 event="click"&#93;
	&#91;tab title="First tab title"&#93;First tab content&#91;/tab&#93;
	&#91;tab title="Second tab title"&#93;Second tab content&#91;/tab&#93;
&#91;/tabs&#93;
</pre>

<h4>Accordion Shortcode Example</h4>

<pre>
&#91;accordions title="Accordion Group Title" active=2 event="click" disabled=false autoheight=true&#93;
	&#91;accordion title="Accordion 1 Title"&#93;First accordion section content&#91;/accordion&#93;
	&#91;accordion title="Accordion 2 Title"&#93;Second accordion section content&#91;/accordion&#93;
&#91;/accordions&#93;
</pre>
				
<h4>Horizontal Accordion Shortcode Example</h4>

<pre>
&#91;haccordions title="Horizontal Accordion Group Title" width=500 height=320 hwidth=28 speed=200 active=1&#93;
	&#91;haccordion title="Fist title"&#93;First accordion content&#91;/haccordion&#93;
	&#91;haccordion title="Second title"&#93;Second accordion content&#91;/haccordion&#93;
&#91;/haccordions&#93;
</pre>
<p>For more details on shortcode syntax visit the <a href="http://thethefly.com/wp-plugins/thethe-tabs-and-accordions/" target="_blank">plugin home page</a>.</p>

