<div class="info">
  <p align="justify"><strong>TheThe Tabs and Accordions</strong> WordPress plugin provides stylish Web 2.0 tools - <em>Tabs</em>, <em>Vertical and Horizontal Accordions</em> and <em>Toggles</em> - to add to your WP blogs. This plugin helps you to make your WordPress website look more aesthetic and professional.</p>

<h3>How does it work?</h3>

<p align="justify">After installing and activating <strong>TheThe Tabs and Accordions</strong> on your WordPress site, simply insert a shortcode on any page, post, and even in the text of a sidebar - wherever you need tabs, toggles, vertical and horizontal accordions to be displayed. In the shortcode, you can set all the parameters of the tool.</p>
</div>
